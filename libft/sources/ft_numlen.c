#include "libft.h"

int	ft_numlen(int n)
{
	int	count;

	if (!n)
		return (1);
	if (n == -2147483648)
		return (11);
	count = 0;
	if (n < 0)
	{
		n *= -1;
		count++;
	}
	while (n > 0)
	{
		n /= 10;
		count++;
	}
	return (count);
}
